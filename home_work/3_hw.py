# Задача 2
# Функция на вход получает два произвольных числа. Вывести в консоль наибольшее из чисел.

def max_num(num_1: int, num_2: int) -> int:
    if num_1 > num_2:
        print('Наибольшее число - ', num_1)
    elif num_1 == num_2:
        print('Числа: ', num_1, ' и ', num_2, ' равны')
    else:
        print('Наибольшее число - ', num_2)
    return 0

max_num(1, 3)
max_num(123, 32)
max_num(-13, 0)
max_num(10.5, 10.5)
print('\n\n')

