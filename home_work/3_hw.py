# Задача 3
# Функция на вход получает два произвольных числа. Вывести в консоль “yes”,
# если они отличаются друг от друга на 135, иначе вывести на экран “No”

def diff_num(num_3: int, num_4: int) -> int:
    difference = num_3-num_4
    if difference == 135 or difference == -135:
        print('yes')
    else:
        print('no')
    return 0

diff_num(135, 0)
diff_num(1350, 14)
diff_num(-135, 0)

print('\n\n')


