# Задача 6
# 6. Функция на вход получает список, состоящий из 5 произвольных чисел.
# Найти количество положительных чисел среди них.

def positive_numbers(num_list :list) -> int:
    sum :int = 0
    for elem in num_list:
        if elem >= 0:
            sum += 1
    print(sum)
    return 0


positive_numbers([33, 2, 4, 5, 6])
positive_numbers([-33, 2, -4, 5, 0])


