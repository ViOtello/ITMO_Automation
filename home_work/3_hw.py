# Задача 4
# Функция на вход получает произвольное число от 1 до 12 (номер месяца).
# Вывести название сезона года в консоль (зима, весна, лето, осень)

def season(month :int) -> str:
    if month == 12 or month == 1 or month == 2:
        print('зима')
    elif month in range(3, 5):
        print('весна')
    elif month in range(6, 8):
        print('лето')
    elif month in range(9, 11):
        print('осень')
    else:
        print('введите номер месяца от 1 до 12')
    return 0

season(3.4)
season(-1)
season(0)
season(12)
season(4)

print('\n\n')


